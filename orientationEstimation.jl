# ╔═╡ da4073b7-abe9-441b-9af1-425b341a2c06
md"""
## Estimate easy axis alignment

In this section of the notebook, the easy axis alignment is estimated.
"""

# ╔═╡ 67b4daed-bf84-45ad-b809-c234a477efea
md"""
### Define estimator function

First, a family of continuous estimator functions for the weights is defined. Here, the vector $p$ hols the three parameters `p[1]`, `p[2]` and `p[3]`. Each function is 180°-periodic and has a single peak of height `p[1]` at `p[2]`.
"""

# ╔═╡ 8be12329-c063-4008-9e97-0b4ba3b0ec8b
@. model(x, p) = p[1]*(cos(π*(x-p[2])/180)^2)^p[3]

# ╔═╡ 5c4414b8-6fd3-4ff5-949c-87450b0ec46f
md"""
### Least squares fit estimator function to data

Second, this function is fitted to the weights. The model parameter `p[2]` (peak location) is extracted as the estimator for the easy axis alignment.
"""

# ╔═╡ 52b7f31e-c9fa-4feb-8490-533accef9f10
function estimateAlignment(initialGuess, weights)
	# the easy axis orientation is used as as initial guess
	# this choice results in a reliable optimization
	# other choices independent on the orientation we aim to estimate are possible
	p0 = [0.0, initialGuess, 1.0]
	# least squares fit
	fit = curve_fit(model, calibrationEasyAxisOrientations, weights, p0, lower=[0.0,0.0,1.0], upper=[Inf,180.0,Inf])
	# optimal parameters
	p = coef(fit)
	# return easy axis alignment estimator
	return p[2]
end

# ╔═╡ 4d3cf4cb-5f5b-476b-a6b1-a1f93aa96a0e
βest = estimateAlignment(β, weights)

# ╔═╡ 4ffb5972-0c52-4be1-aa7b-9dc94ed7c4bb
begin
	# for plotting we need all parameters 
	p0 = [0.0, β, 1.0]
	fit = curve_fit(model, calibrationEasyAxisOrientations, weights, p0, lower=[0.0,0.0,1.0], upper=[Inf,180.0,Inf])
	p = coef(fit)
	
	plt2 = scatter(calibrationEasyAxisOrientations,weights,
