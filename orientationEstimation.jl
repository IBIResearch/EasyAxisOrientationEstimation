# ╔═╡ 70edbec3-82ff-4980-aa2a-c7866a739488
function getWeights(experimentNumber,x,y)
	# calculate sample position in pixel coordinates
	xImagePos = (x .+ fovWidth./2)./(fovWidth./imageWidth)
	yImagePos = (y .+ fovHeight./2)./(fovHeight./imageHeight)

	# load tomogram
	experiment = getExperiment(study,experimentNumber) 
	tomogram = reconstruction(experiment,recoparams)
	
	# create circular mask around sample position
	sumMask = mask(xImagePos,yImagePos) 
	
	# obtain numerical raw data from image object
	rawData = arraydata(arraydata(tomogram))
	
	# mask data
	maskData = rawData.*sumMask
	
	# calculate weights by summing each channel
	weights = vec(sum(maskData, dims=(2,3,4))) 
	return weights
end

# ╔═╡ ad4f7a3d-98e1-408c-a9a6-d35a35f0a905
