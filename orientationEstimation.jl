# ╔═╡ a08fbfdc-d3c0-4e64-8e5b-770893906f5b
# creates discrete circular mask with radius 2 pixels around position x, y
function mask(x,y,imageWidth=imageWidth,imageHeight=imageHeight,radius=2.0)
    mask = zeros(Float64,1,imageWidth,imageHeight,1,1)
    for i in CartesianIndices(mask)
        if (i[2]-x)^2 + (i[3]-y)^2 < radius^2
            mask[i]=1
        end
    end
    return mask
end

