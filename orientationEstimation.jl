# ╔═╡ 1705d7c4-c9bb-41e3-8d15-49fc6fc93e62
md"""
## Calculate estimation errors

The estimation error is calculated for each measurement and its distribution over all measurements visualized.
"""

# ╔═╡ 25bdc400-6681-48e4-97b3-fc614c3ea48c
function calcError(easyAxisOrientation, orientationEstimate)
    err = orientationEstimate-easyAxisOrientation
    # Correction periodic orientations at 180 to 0 degree
    if err > 90
        err = (orientationEstimate-180.0)-easyAxisOrientation
    elseif err < -90
        err = (orientationEstimate+180.0)-easyAxisOrientation
    end
    return err
end

